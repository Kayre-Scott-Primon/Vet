    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        
        Vet.cadas.setVisible(false);
        
        if (nome == null) {
            nome.setText("");
        }
        if (especie == null) {
            especie.setText("");
        }
        if (raca == null) {
            raca.setText("");
        }
        if (idade == null) {
            idade.setText("");
        }
        if (sexo == null) {
            sexo.setText("");
        }
        if (porte == null) {
            porte.setText("");
        }
        if (cor == null) {
            cor.setText("");
        }
        if (peso == null) {
            peso.setText("");
        }

        if ("".equals(nome.getText()) && "".equals(especie.getText()) && "".equals(raca.getText()) && "".equals(idade.getText())&& "".equals(sexo.getText())&& "".equals(porte.getText())&& "".equals(cor.getText())&& "".equals(peso.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha os campos");
        } else {

            Animal a = new Animal(nome.getText(), especie.getText(), raca.getText(), sexo.getText(), cor.getText(), porte.getText(), Integer.parseInt(peso.getText()), Integer.parseInt(idade.getText()));

            if (null == Vet.pesquisarA(nome.getText())) {

                try {
                    Vet.animal.add(a);
                    //Loja.exportarCSVChaves("listaChaves.csv");
                    //Loja.salvarArq(chave);

                    Vet.exportarArqA(a);

                    Vet.cadas.setVisible(false);

                    nome.setText("");

                    especie.setText("");

                    raca.setText("");

                    sexo.setText("");

                    cor.setText("");
                    
                    porte.setText("");
                    
                    idade.setText("");
                    
                    peso.setText("");
                    
                } catch (IOException ex) {
                    Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Ja cadastrado !");
            }
        }
        
        
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

